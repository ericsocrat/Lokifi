def main():
    print(f"Domain Availability Check - {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60)
    print()

    available_domains = []
    taken_domains = []

    for domain in domains_to_check:
        try:
            available = check_domain(domain)
            status = "✓ AVAILABLE" if available else "✗ TAKEN"
            print(f"{domain:<25} {status}")

            if available:
                available_domains.append(domain)
            else:
                taken_domains.append(domain)
        except Exception as e:
            print(f"{domain:<25} ? ERROR - {str(e)}")

    print()
    print("=" * 60)
    print(f"\nSummary:")
    print(f"Available: {len(available_domains)}")
    print(f"Taken: {len(taken_domains)}")
    print()

    if available_domains:
        print("Available domains:")
        for domain in available_domains:
            print(f"  • {domain}")


if __name__ == "__main__":
    main()
